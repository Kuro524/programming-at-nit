/*/////////////////////////////////////////////////////////////////////////////////////
課題１７．for文を使って、'-'と'*'を下記のように逆三角形に並べるプログラムを作成しなさい。
文字は一つずつ出力し、文字と改行を同時に出力しないこと。
**********
-*********
--********
---*******
----******
-----*****
------****
-------***
--------**
---------*

キーワード：for文、二重の繰り返し
最終更新日：2016.03.28
*/
#define _CRT_SECURE_NO_WARNINGS  // 余計な警告が出ないようにする
#include	<stdio.h>

int	main(void)
{
	int	i, j;

	for (i = 0; i < 10; i++) {
		for (j = 0; j < i; j++) {
			printf("-");
		}
		for (; j < 10; j++) {
			printf("*");
		}
		printf("\n");
	}
    
	return 0;
}
